#!/usr/bin/env python3
"""Score the latest training run against the committed eval cases.

Mirrors the TS-side `just eval` semantics so Phase B numbers are directly
comparable to the existing engine snapshots in docs/eval/. For each eval case,
encodes the query, cosine-ranks against precomputed library embeddings, and
checks whether the top-1 / top-3 family matches the case's expectedFamily (or
top-1 name for cases keyed on expectedName).

Inputs (regenerated by `pnpm tsx scripts/dump-for-training.ts`):
  training/data/eval-cases.json
  training/data/colors-small.json

The model is loaded from training/runs/<LATEST>/model/ unless --run is passed.
"""
from __future__ import annotations

import argparse
import json
import logging
import sys
from collections import defaultdict
from pathlib import Path

import numpy as np
import torch
from sentence_transformers import SentenceTransformer


ROOT = Path(__file__).resolve().parent
RUNS_DIR = ROOT / "runs"
DATA_DIR = ROOT / "data"
EVAL_CASES_PATH = DATA_DIR / "eval-cases.json"
LIBRARY_PATH = DATA_DIR / "colors-small.json"

# How to encode a library entry for the query→library cosine. Options:
#   hex   — "the colour {hex}" (matches training prompt 2 byte-for-byte)
#   name  — "{name}" (leans on pretrained semantics; library hexes don't appear in training)
#   family — "{family} colour" (only 16 distinct strings; can't rank within family)
LIBRARY_TEMPLATES = {
    "hex": "the colour {hex}",
    "name": "{name}",
    "family": "{family} colour",
}


def resolve_run_dir(run: str | None) -> Path:
    if run:
        candidate = (RUNS_DIR / run) if not Path(run).is_absolute() else Path(run)
    else:
        latest_pointer = RUNS_DIR / "LATEST"
        if not latest_pointer.exists():
            raise SystemExit(
                f"No --run given and {latest_pointer} not found. Train a model first with `just train`."
            )
        candidate = RUNS_DIR / latest_pointer.read_text().strip()
    if not (candidate / "model").exists():
        raise SystemExit(f"No model/ under {candidate}. Did training complete?")
    return candidate


def pick_device() -> str:
    if torch.backends.mps.is_available():
        return "mps"
    if torch.cuda.is_available():
        return "cuda"
    return "cpu"


def load_json(path: Path):
    if not path.exists():
        raise SystemExit(
            f"{path} not found. Run `pnpm tsx scripts/dump-for-training.ts` first "
            "(or use `just train-eval`, which chains the dump)."
        )
    return json.loads(path.read_text())


def score_case(case: dict, top_results: list[dict]) -> tuple[bool, bool]:
    """Compute (pass_at_1, pass_at_3) using the same logic as the TS runner."""
    expected_family = case.get("expectedFamily")
    expected_name = case.get("expectedName")
    if expected_name:
        # literal-name cases: match by exact name (case-insensitive)
        pass_at_1 = top_results[0]["name"].lower() == expected_name.lower()
        pass_at_3 = any(r["name"].lower() == expected_name.lower() for r in top_results[:3])
    elif expected_family:
        pass_at_1 = top_results[0]["family"] == expected_family
        pass_at_3 = any(r["family"] == expected_family for r in top_results[:3])
    else:
        # No expectation (e.g., poetic) — exclude from scoring
        pass_at_1 = False
        pass_at_3 = False
    return pass_at_1, pass_at_3


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument(
        "--run",
        default=None,
        help="Run directory name (e.g. 20260517-044552) under training/runs/. Defaults to runs/LATEST.",
    )
    parser.add_argument(
        "--batch-size",
        type=int,
        default=64,
        help="Encoder batch size when computing the library embeddings.",
    )
    parser.add_argument(
        "--library-encoding",
        choices=tuple(LIBRARY_TEMPLATES.keys()),
        default="name",
        help="How to encode library entries for the query→library cosine. Default: name.",
    )
    parser.add_argument(
        "--out",
        type=Path,
        default=None,
        help="Optional path to write the per-case results JSON. Defaults to <run>/eval-results.json.",
    )
    args = parser.parse_args()

    logging.basicConfig(level=logging.INFO, format="%(asctime)s  %(levelname)s  %(message)s")
    log = logging.getLogger("phase-b-eval")

    run_dir = resolve_run_dir(args.run)
    log.info("Run: %s", run_dir.name)

    eval_cases = load_json(EVAL_CASES_PATH)
    library = load_json(LIBRARY_PATH)
    log.info("Loaded %d eval cases and %d library entries.", len(eval_cases), len(library))

    device = pick_device()
    log.info("Device: %s", device)
    model = SentenceTransformer(str(run_dir / "model"), device=device)

    template = LIBRARY_TEMPLATES[args.library_encoding]
    log.info("Library encoding: %s → %r", args.library_encoding, template)
    # Encode library once, normalised so the cosine becomes a dot product.
    library_texts = [template.format(**entry) for entry in library]
    library_emb = model.encode(
        library_texts,
        batch_size=args.batch_size,
        convert_to_numpy=True,
        normalize_embeddings=True,
        show_progress_bar=False,
    )
    log.info("Encoded library (%s vectors, dim=%d).", library_emb.shape[0], library_emb.shape[1])

    queries = [case["query"] for case in eval_cases]
    query_emb = model.encode(
        queries,
        batch_size=args.batch_size,
        convert_to_numpy=True,
        normalize_embeddings=True,
        show_progress_bar=False,
    )

    # Cosine = normalised dot product. Argsort descending for top-K.
    scores = query_emb @ library_emb.T
    top_k_indices = np.argsort(-scores, axis=1)[:, :3]

    per_case_results: list[dict] = []
    totals = defaultdict(lambda: {"n": 0, "at1": 0, "at3": 0})

    for case, top_idx, row_scores in zip(eval_cases, top_k_indices, scores, strict=True):
        top_entries = [
            {
                "name": library[int(i)]["name"],
                "hex": library[int(i)]["hex"],
                "family": library[int(i)]["family"],
                "score": float(row_scores[int(i)]),
            }
            for i in top_idx
        ]
        category = case["category"]
        if case.get("expectedFamily") or case.get("expectedName"):
            pass_at_1, pass_at_3 = score_case(case, top_entries)
            totals[category]["n"] += 1
            totals[category]["at1"] += int(pass_at_1)
            totals[category]["at3"] += int(pass_at_3)
            totals["__overall__"]["n"] += 1
            totals["__overall__"]["at1"] += int(pass_at_1)
            totals["__overall__"]["at3"] += int(pass_at_3)
            scored = True
        else:
            pass_at_1 = pass_at_3 = False
            scored = False

        per_case_results.append({
            "query": case["query"],
            "category": category,
            "expectedFamily": case.get("expectedFamily"),
            "expectedName": case.get("expectedName"),
            "scored": scored,
            "results": top_entries,
            "pass": {"at1": pass_at_1, "at3": pass_at_3},
        })

    # Pretty-print summary
    print()
    print(f"{'Category':<22} {'n':>4} {'acc@1':>8} {'acc@3':>8}")
    print("-" * 46)
    ordered_categories = sorted(c for c in totals if c != "__overall__")
    for category in ordered_categories:
        row = totals[category]
        n = row["n"]
        at1 = row["at1"] / n if n else 0
        at3 = row["at3"] / n if n else 0
        print(f"{category:<22} {n:>4} {at1:>7.0%} {at3:>7.0%}")
    print("-" * 46)
    overall = totals["__overall__"]
    n = overall["n"]
    at1 = overall["at1"] / n if n else 0
    at3 = overall["at3"] / n if n else 0
    print(f"{'OVERALL':<22} {n:>4} {at1:>7.0%} {at3:>7.0%}")
    print()

    out_path = args.out or (run_dir / f"eval-results-{args.library_encoding}.json")
    out_path.write_text(json.dumps({
        "run": run_dir.name,
        "library_encoding": args.library_encoding,
        "library_template": template,
        "totals": {k: dict(v) for k, v in totals.items()},
        "cases": per_case_results,
    }, indent=2))
    log.info("Wrote per-case results → %s", out_path)

    return 0


if __name__ == "__main__":
    sys.exit(main())
